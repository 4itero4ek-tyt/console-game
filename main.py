def pause(text):
    """–ö—Ä–∞—Å–∏–≤—ã–π –≤—ã–≤–æ–¥ —Ç–µ–∫—Å—Ç–∞ —Å –∑–∞–¥–µ—Ä–∂–∫–æ–π"""
    print(text)
    time.sleep(1.3)

def show_inventory():
    """–ü–æ–∫–∞–∑—ã–≤–∞–µ—Ç –∫–∞–∫–∏–µ –ø—Ä–µ–¥–º–µ—Ç—ã –≤ —Ä—é–∫–∑–∞–∫–µ"""
    
    if inventory:
        print("\nüéí –í —Ç–≤–æ—ë–º —Ä—é–∫–∑–∞–∫–µ:", ", ".join(inventory))
    else:
        print("\nüéí –†—é–∫–∑–∞–∫ –ø—É—Å—Ç.")

def describe_item(item):
    """–í—ã–≤–æ–¥ –æ–ø–∏—Å–∞–Ω–∏–µ –ø—Ä–µ–¥–º–µ—Ç–∞"""

    if item in items_description:
        print(f"üìò {items_description[item]}")


def level_one():
    """–£—Ä–æ–≤–µ–Ω—å 1 ‚Äî –ø–æ–±–µ–≥ –∏–∑ –≥–æ—Ä–æ–¥–∞"""

    pause("\n*** –£–†–û–í–ï–ù–¨ 1: –ü–û–ë–ï–ì –ò–ó –ì–û–†–û–î–ê ***")
    pause("–¢—ã –ø—Ä–æ—Å—ã–ø–∞–µ—à—å—Å—è –≤ –∑–∞–±—Ä–æ—à–µ–Ω–Ω–æ–π –∫–≤–∞—Ä—Ç–∏—Ä–µ. –°–Ω–∞—Ä—É–∂–∏ —Å–ª—ã—à–Ω—ã —Å—Ç–æ–Ω—ã –∑–æ–º–±–∏...")
    pause("–ü–µ—Ä–µ–¥ —Ç–æ–±–æ–π –ª–µ–∂–∞—Ç –ø—Ä–µ–¥–º–µ—Ç—ã: –Ω–æ–∂, –∞–ø—Ç–µ—á–∫–∞, –∫–∞–º–µ–Ω—å, –∫–∞—Ä—Ç–∞.")
    
    objects = ["–Ω–æ–∂", "–∞–ø—Ç–µ—á–∫–∞", "–∫–∞–º–µ–Ω—å", "–∫–∞—Ä—Ç–∞"]
    
    while True:
        action = input("\n–ß—Ç–æ —Å–¥–µ–ª–∞—Ç—å? (–≤–∑—è—Ç—å [–ø—Ä–µ–¥–º–µ—Ç] / –≤—ã–π—Ç–∏): ").lower()
        if action.startswith("–≤–∑—è—Ç—å"):
            item = action.replace("–≤–∑—è—Ç—å ", "").strip()
            if item in objects:
                inventory.append(item)
                pause(f"–¢—ã –≤–∑—è–ª –ø—Ä–µ–¥–º–µ—Ç: {item}.")
                describe_item(item)
                objects.remove(item)
            else:
                print("–¢–∞–∫–æ–≥–æ –ø—Ä–µ–¥–º–µ—Ç–∞ –∑–¥–µ—Å—å –Ω–µ—Ç.")
        elif action == "–≤—ã–π—Ç–∏":
            pause("–¢—ã –æ—Å—Ç–æ—Ä–æ–∂–Ω–æ –≤—ã–≥–ª—è–¥—ã–≤–∞–µ—à—å –≤ –æ–∫–Ω–æ...")
            break
        else:
            print("–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–∞—è –∫–æ–º–∞–Ω–¥–∞.")
    
    if "–Ω–æ–∂" in inventory:
        pause("–¢—ã –æ—Ç–±–∏–≤–∞–µ—à—å—Å—è –Ω–æ–∂–æ–º –∏ —É–±–µ–≥–∞–µ—à—å –≤ —Å—Ç–æ—Ä–æ–Ω—É –≤—ã–∂–∏–≤—à–∏—Ö!")
        pause("–¢—ã –¥–æ–±—Ä–∞–ª—Å—è –¥–æ –±–µ–∑–æ–ø–∞—Å–Ω–æ–≥–æ –¥–æ–º–∞.")
    else:
        pause("–¢—ã –±–µ–∑–æ—Ä—É–∂–µ–Ω... –∑–æ–º–±–∏ —Ç–µ–±—è –¥–æ–≥–æ–Ω—è–µ—Ç!")
        pause("–ö–æ–Ω–µ—Ü –∏–≥—Ä—ã ‚Äî –ø–æ–ø—Ä–æ–±—É–π —Å–Ω–æ–≤–∞.")
        exit()
    if "–∫–∞—Ä—Ç–∞" not in inventory:
        pause("–¢—ã –≤—ã–±–µ–≥–∞–µ—à—å –∏–∑ –¥–æ–º–∞, –Ω–æ –Ω–µ –∑–Ω–∞–µ—à—å –¥–æ—Ä–æ–≥–∏. –£–ª–∏—Ü—ã –ø–µ—Ä–µ–ø–æ–ª–Ω–µ–Ω—ã –∑–æ–º–±–∏.")
        pause("–¢—ã –±–µ–∂–∏—à—å –∫—É–¥–∞ –ø–æ–ø–∞–ª–æ... —Ç–µ—Ä—è–µ—à—å —Å–∏–ª—ã –∏ –ø–∞–¥–∞–µ—à—å –±–µ–∑ —Å–æ–∑–Ω–∞–Ω–∏—è.")
        pause("–ë–µ–∑ –∫–∞—Ä—Ç—ã —Ç—ã –∑–∞–±–ª—É–¥–∏–ª—Å—è. –ö–æ–Ω–µ—Ü –∏–≥—Ä—ã.")
        exit()
    else:
        pause("–ë–ª–∞–≥–æ–¥–∞—Ä—è –∫–∞—Ä—Ç–µ —Ç—ã –Ω–∞—Ö–æ–¥–∏—à—å –±–µ–∑–æ–ø–∞—Å–Ω—ã–π –º–∞—Ä—à—Ä—É—Ç –∫ –ø–æ–¥–≤–∞–ª—É –≤—ã–∂–∏–≤—à–∏—Ö!")
        pause(f"–ù–∞ –∫–∞—Ä—Ç–µ —É–∫–∞–∑–∞–Ω–æ —É–±–µ–∂–∏—â–µ –ø–æ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç–∞–º: {safe_zone_coords}.")
        pause("–¢—ã –æ—Å—Ç–æ—Ä–æ–∂–Ω–æ –Ω–∞–ø—Ä–∞–≤–ª—è–µ—à—å—Å—è —Ç—É–¥–∞...")


def level_two():
    """–£—Ä–æ–≤–µ–Ω—å 2 ‚Äî –ø–æ–¥–≤–∞–ª –≤—ã–∂–∏–≤—à–∏—Ö"""
    pause("\n*** –£–†–û–í–ï–ù–¨ 2: –ü–û–î–í–ê–õ –í–´–ñ–ò–í–®–ò–• ***")
    pause("–¢—ã –Ω–∞—Ö–æ–¥–∏—à—å –≤—Ö–æ–¥ –≤ –ø–æ–¥–≤–∞–ª, –Ω–æ –≤–Ω—É—Ç—Ä–∏ —Ç–µ–º–Ω–æ—Ç–∞.")
    pause("–¢—ã –≤–∏–¥–∏—à—å —É –≤—Ö–æ–¥–∞ –Ω–µ—Å–∫–æ–ª—å–∫–æ –ø—Ä–µ–¥–º–µ—Ç–æ–≤: –±–∞—Ç–∞—Ä–µ–π–∫–∞, —Ñ–æ–Ω–∞—Ä—å.")
    
    items = ["–±–∞—Ç–∞—Ä–µ–π–∫–∞", "—Ñ–æ–Ω–∞—Ä—å"]
    key_found = False
    light_on = False
    
    while True:
        show_inventory()
        action = input("\n–ß—Ç–æ —Å–¥–µ–ª–∞—Ç—å? (–≤–∑—è—Ç—å [–ø—Ä–µ–¥–º–µ—Ç] / –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å [–ø—Ä–µ–¥–º–µ—Ç]): ").lower()
        
        # –í–∑—è—Ç–∏–µ –ø—Ä–µ–¥–º–µ—Ç–æ–≤
        if action.startswith("–≤–∑—è—Ç—å"):
            item = action.replace("–≤–∑—è—Ç—å ", "").strip()
            if item in items:
                inventory.append(item)
                pause(f"–¢—ã –ø–æ–¥–æ–±—Ä–∞–ª {item}.")
                describe_item(item)
                items.remove(item)
            else:
                print("–¢–∞–∫–æ–≥–æ –ø—Ä–µ–¥–º–µ—Ç–∞ –∑–¥–µ—Å—å –Ω–µ—Ç.")
        
        # –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ –ø—Ä–µ–¥–º–µ—Ç–æ–≤
        elif action.startswith("–∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å"):
            item = action.replace("–∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å ", "").strip()
            
            if item == "—Ñ–æ–Ω–∞—Ä—å" in inventory:
                if "–±–∞—Ç–∞—Ä–µ–π–∫–∞" in inventory:
                    pause("–¢—ã –≤—Å—Ç–∞–≤–ª—è–µ—à—å –±–∞—Ç–∞—Ä–µ–π–∫—É –≤ —Ñ–æ–Ω–∞—Ä—å... —Å–≤–µ—Ç –≤—Å–ø—ã—Ö–∏–≤–∞–µ—Ç! –¢–µ–ø–µ—Ä—å –≤–∏–¥–Ω–æ –≤ –ø–æ–¥–≤–∞–ª–µ.")
                    light_on = True
                    pause("–ù–∞ –ø–æ–ª—É —Ç—ã –∑–∞–º–µ—á–∞–µ—à—å –º–µ—Ç–∞–ª–ª–∏—á–µ—Å–∫–∏–π –±–ª–µ—Å–∫ ‚Äî —ç—Ç–æ –∫–ª—é—á!")
                    if not key_found:
                        inventory.append("–∫–ª—é—á")
                        describe_item("–∫–ª—é—á")
                        key_found = True
                else:
                    pause("–§–æ–Ω–∞—Ä—å –Ω–µ —Ä–∞–±–æ—Ç–∞–µ—Ç ‚Äî –Ω—É–∂–Ω–∞ –±–∞—Ç–∞—Ä–µ–π–∫–∞.")
            
            elif item == "–∫–ª—é—á" and "–∫–ª—é—á" in inventory:
                if light_on:
                    pause("–¢—ã –≤—Å—Ç–∞–≤–ª—è–µ—à—å –∫–ª—é—á –≤ –∑–∞–º–æ–∫... —â–µ–ª—á–æ–∫! –î–≤–µ—Ä—å –æ—Ç–∫—Ä—ã—Ç–∞.")
                    break
                else:
                    pause("–°–ª–∏—à–∫–æ–º —Ç–µ–º–Ω–æ, —Ç—ã –Ω–µ –º–æ–∂–µ—à—å –Ω–∞–π—Ç–∏ –∑–∞–º–æ–∫.")
            
            else:
                print("–≠—Ç–æ—Ç –ø—Ä–µ–¥–º–µ—Ç –Ω–µ–ª—å–∑—è –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –∑–¥–µ—Å—å.")
        
        else:
            print("–ö–æ–º–∞–Ω–¥–∞ –Ω–µ —Ä–∞—Å–ø–æ–∑–Ω–∞–Ω–∞.")
    
    pause("–¢—ã –≤—Ö–æ–¥–∏—à—å –≤ –∫–æ–º–Ω–∞—Ç—É... –≤–Ω—É—Ç—Ä–∏ –∑–∞–ø–∞—Å –µ–¥—ã –∏ –æ—Ä—É–∂–∏–µ!")
    pause("–¢—ã –≤—ã–∂–∏–ª! –ü–æ–∑–¥—Ä–∞–≤–ª—è–µ–º, —Ç—ã –ø—Ä–æ—à—ë–ª –¥–≤–∞ —É—Ä–æ–≤–Ω—è –∏–≥—Ä—ã!")
    show_inventory()
    pause("\n–ö–æ–Ω–µ—Ü –∏–≥—Ä—ã.")



